# Pedir al usuario que ingrese dos números y mostrar la suma de los dos números.

print("Ingres dos numero enteros y muestra su suma")

numero1 = int(input("Ingresa el primer numero entero: "))
numero2 = int(input("Ingresa el segundo numero entero: "))

suma = numero1 + numero2

print("La suma de los valores es: "+str(suma))