# Pedir al usuario que ingrese dos números y mostrar el cociente de los dos números.
print("Pasar dos enteros y que nos devuelva el cociente")

numero1 = int(input("Dame el primer entero: "))
numero2 = int(input("Dame el segundo entero: "))

cociente = numero1 / numero2
print("El cociente de los valores es: "+str(cociente))
