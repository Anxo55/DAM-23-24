# Pedir al usuario que ingrese dos números y mostrar la resta de los dos números.
print("Pasar don numeros y que nos devuelva la resta")

numero1 = int(input("Ingresa el primer entero: "))
numero2 = int(input("Ingresa el segundo entero: "))

resta = numero1 - numero2

print("La resta de los valores es: "+str(resta))